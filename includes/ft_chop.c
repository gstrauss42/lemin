#include "../libft/libft.h"
#include "include.h"

void	ft_chop(char ***str)
{
	int i;

	i = 1;
	while((*str)[i])
	{
		(*str)[i - 1] = (*str)[i];
		i++;
	}
	(*str)[i - 1] = NULL;
}
